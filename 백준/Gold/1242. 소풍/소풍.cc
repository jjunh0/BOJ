void solve() {
  int n, K, m, ans = 0;
  cin >> n >> K >> m;
  
  while(n > 0) {
    int k = K%(n);
    if(k == 0) k = n;
    ans ++;
    if(m == k) {
      break;
    }
    if(m > k) m = m - k;
    else m = n-k+m;
    n --;
  }
  cout << ans;
}

